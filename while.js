let num = 1;
//enquanto a variavel num for menor ou igual a 10, acrescente um numero
while (num <= 10) {
    console.log(num++)
}

//é executado 50 vezes, do 1 ao 50
for (let cont = 1; cont <= 50; cont++) {
    console.log(cont);
}

//é executado de traz para frente, do 10 ao 1
for (cont = 10; cont > 0; cont--) {
    console.log(cont)
}